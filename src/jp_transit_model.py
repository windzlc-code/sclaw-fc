def _sort_by_custom_order(values: list[str], order: list[str]) -> list[str]:
    pos = {v: i for i, v in enumerate(order)}
    return sorted(values, key=lambda x: (pos.get(x, 999), x))


def _derived_station_seed_from_suumo_lines() -> list[tuple[int, int, str, str, str, str, str]]:
    rows: list[tuple[int, int, str, str, str, str, str]] = []
    for line_id, city_area, _typ, _line_name, _color, main_ward in JP_TRANS_SUUMO_STYLE_LINE_SEED:
        names = [x.strip() for x in str(main_ward or "").replace("、", ",").split(",") if x.strip()]
        for idx, name in enumerate(names[:18], start=1):
            sid = int(line_id) * 100 + idx
            rows.append((sid, int(line_id), name, city_area, city_area, "", name))
    return rows


