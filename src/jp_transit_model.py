def ensure_jp_transit_schema(conn: sqlite3.Connection) -> None:
    conn.executescript(
        """
        CREATE TABLE IF NOT EXISTS jp_trans_line (
            line_id INTEGER PRIMARY KEY,
            city_area TEXT NOT NULL,
            trans_type TEXT NOT NULL,
            line_name TEXT NOT NULL,
            line_color TEXT NOT NULL DEFAULT '',
            main_ward TEXT NOT NULL DEFAULT ''
        );
        CREATE INDEX IF NOT EXISTS idx_jp_trans_line_area ON jp_trans_line(city_area);
        CREATE INDEX IF NOT EXISTS idx_jp_trans_line_type ON jp_trans_line(city_area, trans_type);

        CREATE TABLE IF NOT EXISTS jp_trans_station (
            station_id INTEGER PRIMARY KEY,
            line_id INTEGER NOT NULL,
            station_name TEXT NOT NULL,
            prefecture TEXT NOT NULL DEFAULT '',
            city TEXT NOT NULL DEFAULT '',
            ward TEXT NOT NULL DEFAULT '',
            full_address TEXT NOT NULL DEFAULT '',
            FOREIGN KEY (line_id) REFERENCES jp_trans_line(line_id)
        );
        CREATE INDEX IF NOT EXISTS idx_jp_trans_station_line ON jp_trans_station(line_id);
        CREATE INDEX IF NOT EXISTS idx_jp_trans_station_name ON jp_trans_station(station_name);
        """
    )


