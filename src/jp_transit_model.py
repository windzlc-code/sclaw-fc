def list_lines(conn: sqlite3.Connection, city_area: str, trans_type: str) -> list[dict[str, Any]]:
    # UI dropdown only needs (line_id, line_name). Keep payload small for speed.
    rows = conn.execute(
        """
        SELECT line_id, line_name
        FROM jp_trans_line
        WHERE city_area = ? AND trans_type = ?
        ORDER BY line_id ASC
        """,
        (city_area, trans_type),
    ).fetchall()
    return [dict(r) for r in rows]


def list_stations(conn: sqlite3.Connection, line_id: int) -> list[dict[str, Any]]:
    # UI dropdown only needs (station_id, station_name). Keep payload small for speed.
    rows = conn.execute(
        """
        SELECT station_id, station_name
        FROM jp_trans_station
        WHERE line_id = ?
        ORDER BY station_name COLLATE NOCASE
        """,
        (int(line_id),),
    ).fetchall()
    return [dict(r) for r in rows]


def get_line_row(conn: sqlite3.Connection, line_id: int) -> dict[str, Any] | None:
    row = conn.execute("SELECT * FROM jp_trans_line WHERE line_id = ?", (int(line_id),)).fetchone()
    return dict(row) if row else None


def get_station_row(conn: sqlite3.Connection, station_id: int) -> dict[str, Any] | None:
    row = conn.execute(
        """
        SELECT s.*, l.line_name, l.city_area, l.trans_type, l.line_color, l.main_ward AS line_main_ward
        FROM jp_trans_station s
        JOIN jp_trans_line l ON l.line_id = s.line_id
        WHERE s.station_id = ?
        """,
        (int(station_id),),
    ).fetchone()
    return dict(row) if row else None


def keyword_boost_for_line(conn: sqlite3.Connection, line_id: int) -> str:
    row = get_line_row(conn, line_id)
    if not row:
        return ""
    parts = [str(row.get("line_name") or ""), str(row.get("main_ward") or ""), str(row.get("city_area") or "")]
    return " ".join(p for p in parts if p).strip()


def keyword_boost_for_station(conn: sqlite3.Connection, station_id: int) -> str:
    row = get_station_row(conn, station_id)
    if not row:
        return ""
    parts = [
        str(row.get("station_name") or ""),
        str(row.get("line_name") or ""),
        str(row.get("ward") or ""),
        str(row.get("prefecture") or ""),
        str(row.get("city") or ""),
    ]
    return " ".join(p for p in parts if p).strip()


