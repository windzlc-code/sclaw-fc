def flatten_smart_nav_keywords() -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for _k, g in SMART_NAV_KEYWORD_GROUPS.items():
        for w in g.get("keywords") or []:
            s = str(w).strip()
            if not s:
                continue
            low = s.lower()
            if low in seen:
                continue
            seen.add(low)
            out.append(s)
    return out


def pick_rotating_seed_queries(*, max_n: int = 8) -> list[str]:
    """依日期／小時輪播一批種子，避免單次 CSE 查詢過多。"""
    from datetime import datetime, timedelta, timezone

    flat = flatten_smart_nav_keywords()
    n = len(flat)
    if n == 0:
        return []
    max_n = max(1, min(12, int(max_n)))
    tw = timezone(timedelta(hours=8))
    now = datetime.now(tw)
    # 以年月日與小時偏移，讓中午與凌晨兩輪觸及不同切片
    seed = now.year * 10000 + now.month * 100 + now.day + now.hour * 7
    start = seed % n
    out: list[str] = []
    for i in range(max_n):
        out.append(flat[(start + i) % n])
    return out


def dimension_label(key: str) -> str:
    g = SMART_NAV_KEYWORD_GROUPS.get((key or "").strip())
    if not g:
        return ""
    return str(g.get("label") or "")
